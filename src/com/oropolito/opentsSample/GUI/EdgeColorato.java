import java.awt.Color;

public class EdgeColorato {
	public double x1=0,x2=0,y1=0,y2=0;
	public Color color;
	public EdgeColorato( double x1,double y1, double x2, double y2, Color color) {
		this.x1=x1;
		this.x2=x2;
		this.y1=y1;
		this.y2=y2;
		this.color = color;
	}
}
